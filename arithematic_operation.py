

import numpy as np

a=np.array([[1,2,3],[2,3,4],[4,5,6]])
b=np.array([[0,9,8],[7,6,5],[9,5,2]])

#addition
c=np.add(a,b)
print(c)

#subtraction
d=np.subtract(a,b)
print(d)

#Multiplication
e=np.multiply(a,b)
print(e)

#division
f=np.divide(a,b)
print(f)

#square
g=np.square(a)
print(g)

#square_root
h=np.sqrt(b)
print(h)